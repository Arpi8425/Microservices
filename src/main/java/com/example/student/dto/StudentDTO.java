package com.example.student.dto;

public class StudentDTO {
    private Long id;
    private String name;
    private int age;
    private String course;

    public StudentDTO() {}

    public StudentDTO(Long id, String name, int age, String course) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.course = course;
    }

    public StudentDTO(String name, int age, String course) {
        this(null, name, age, course);
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getCourse() {
        return course;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setCourse(String course) {
        this.course = course;
    }
}
